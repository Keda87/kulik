object ArithmeticOperator {

  var a = 10
  var b = 20

  def main(args: Array[String]) {
    println("a + b = " + (a + b))
    println("a - b = " + (a - b))
    println("a * b = " + (a * b))
    println("a / b = " + (a / b))
    println("a % b = " + (a % b))
  }
}
